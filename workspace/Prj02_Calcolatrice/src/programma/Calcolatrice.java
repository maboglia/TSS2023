package programma;

public class Calcolatrice {
	
	public static double addizione(int a, int b) {
		return a + b;
	}
	public static double sottrazione(int a, int b) {
		return a - b;
	}
	public static double moltiplicazione(int a, int b) {
		return a * b;
	}
	public static double divisione(int a, int b) {
		return (double) a / b;
	}
	
}
